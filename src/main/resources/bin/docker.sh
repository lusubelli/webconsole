
export DOCKER_IMAGE_NAME=user-api
export DOCKER_IMAGE_VERSION=1.0.0

export DOCKER_LOGIN=developer
export DOCKER_PASSWORD=$(oc whoami -t)
export DOCKER_REGISTRY=127.0.0.1:5000
export DOCKER_REPOSITORY=accounting-dev

docker login -u $DOCKER_LOGIN -p $DOCKER_PASSWORD $DOCKER_REGISTRY
docker build -t $DOCKER_REGISTRY/$DOCKER_REPOSITORY/$DOCKER_IMAGE_NAME:$DOCKER_IMAGE_VERSION .
docker push $DOCKER_REGISTRY/$DOCKER_REPOSITORY/$DOCKER_IMAGE_NAME:$DOCKER_IMAGE_VERSION
docker logout
